
<div class="highlight-box">
    <h3>Funded childcare</h3>
    <p>
        From September 2025, eligible working parents of children from 9 months
        old up to school age will be entitled to <b
            >30 hours of funded childcare per week during term time</b
        > (38 weeks per year).
    </p>
</div>

<h2>How to Access Your Free Hours</h2>
<div class="steps">
    <p>To use your funded hours with me, you'll need to:</p>
    <ol>
        <li>
            <strong>Check your eligibility</strong> on the government's
            <a href="https://www.childcarechoices.gov.uk">Childcare Choices</a> Childcare
            Choices website
        </li>
        <li>
            <strong>Apply online</strong> through
            <a href="gov.uk">gov.uk</a> using your Government Gateway account
        </li>
        <li>
            <strong>Receive your eligibility code</strong> - this confirms your entitlement
        </li>
        <li>
            <strong>Share your code with me</strong> so I can claim the funding on
            your behalf
        </li>
        <li>
            <strong>Reconfirm every 3 months</strong> to maintain your eligibility
        </li>
    </ol>
</div>

<h2>Working Parent Eligibility Requirements</h2>
<p>
    To qualify for the expanded funded hours, you (and your partner if you have
    one) must:
</p>
<ul>
    <li>
        Work and earn, or expect to earn over the next 3 months, at least the
        equivalent of working 16 hours per week at national minimum or living
        wage
    </li>
    <li>Not have an adjusted net income exceeding Â£100,000 for the tax year</li>
    <li>
        Both meet these requirements (unless exceptions apply, such as one
        partner being on parental leave)
    </li>
</ul>

<h2>When Your Child Becomes Eligible</h2>
<div class="important-note">
    Children become eligible from the term after they reach the relevant age.
    Terms start on 1 January, 1 April, and 1 September.
</div>

<h2>How I Support All Funded Hours</h2>
<h3>No hidden costs</h3>
<p>
    Your funded hours are completely free. I don't charge 'top-up' fees (any
    difference between my normal charge and the funding I receive from the local
    authority).
</p>

<h3>Transparent additional charges</h3>
<p>
    While core childcare is free, I charge separately for meals, nappies, sun
    cream, and optional activities like trips. All charges are clearly itemised
    on invoices.
</p>

<h3>Quality care maintained</h3>
<p>
    Whether you're using funded hours or paying privately, your child receives
    the same high-quality care, activities, and attention.
</p>

<h2>What's Included in Your Free Hours</h2>
<ul>
    <li>All learning activities and educational play</li>
    <li>Use of toys, books, and educational resources</li>
    <li>Outdoor play and garden activities</li>
    <li>Regular updates on your child's development</li>
    <li>Professional early years education</li>
</ul>

<h2>What May Incur Additional Charges</h2>
<p>
    I can charge separately for meals and snacks, consumables such as nappies or
    sun cream, and services such as trips and specialist activities. These are
    clearly outlined in my fees policy and itemised on invoices.
</p>

